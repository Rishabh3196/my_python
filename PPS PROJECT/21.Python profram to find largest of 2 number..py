# 21.Python program to find largest of 2 number.

a = float(input("Enter the first 2 Digit Number :"))
b = float(input("Enter the second 2 Digit Number :"))
print("First Number = ", a)
print("Second Number = ", b)
if a > b:
    print(a, "is the largest number")
else:
    print(b, "is the largest number")